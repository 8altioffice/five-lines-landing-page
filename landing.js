function renderContent(lang) {
    const t = translations[lang] || translations.en;

    // Safety check: merge with English to ensure no missing keys
    const safeT = {
        ...translations.en, ...t,
        hero: { ...translations.en.hero, ...t.hero },
        benefits: { ...translations.en.benefits, ...t.benefits },
        how: { ...translations.en.how, ...t.how },
        faq: { ...translations.en.faq, ...t.faq },
        cta: { ...translations.en.cta, ...t.cta },
        features: { ...translations.en.features, ...t.features },
        reviews: { ...translations.en.reviews, ...t.reviews },
        footer: { ...translations.en.footer, ...t.footer }
    };

    // Update Hero
    setText('hero-title', safeT.hero.title);
    setText('hero-subtitle', safeT.hero.subtitle);

    // Update Benefits
    setText('benefits-title', safeT.benefits.title);
    setText('benefit1-title', safeT.benefits.b1_title);
    setText('benefit1-desc', safeT.benefits.b1_desc);
    setText('benefit2-title', safeT.benefits.b2_title);
    setText('benefit2-desc', safeT.benefits.b2_desc);
    setText('benefit3-title', safeT.benefits.b3_title);
    setText('benefit3-desc', safeT.benefits.b3_desc);

    // Update How It Works
    setText('how-title', safeT.how.title);
    setHTML('step1', safeT.how.step1);
    setHTML('step2', safeT.how.step2);
    setHTML('step3', safeT.how.step3);

    // Update Features
    setText('feat-offline', safeT.features.offline);
    setText('feat-haptics', safeT.features.haptics);
    setText('feat-modes', safeT.features.modes);
    setText('feat-controls', safeT.features.controls);

    // Update Reviews
    setText('reviews-title', safeT.reviews.title);
    setText('review1', safeT.reviews.r1);
    setText('review2', safeT.reviews.r2);
    setText('review3', safeT.reviews.r3);

    // Update FAQ
    setText('faq-title', safeT.faq.title);
    setText('faq1-q', safeT.faq.q1);
    setText('faq1-a', safeT.faq.a1);
    setText('faq2-q', safeT.faq.q2);
    setText('faq2-a', safeT.faq.a2);
    setText('faq3-q', safeT.faq.q3);
    setText('faq3-a', safeT.faq.a3);

    // Update CTA
    setText('cta-title', safeT.cta.title);
    setText('cta-subtitle', safeT.cta.subtitle);

    // Update Footer
    setText('link-privacy', safeT.footer.privacy);
    setText('link-support', safeT.footer.support);
}

function setText(id, text) {
    const el = document.getElementById(id);
    if (el) el.textContent = text;
}

function setHTML(id, html) {
    const el = document.getElementById(id);
    if (el) el.innerHTML = html;
}

// Event Listeners
document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => {
        document.querySelectorAll('.lang-btn').forEach(b => b.classList.remove('active'));
        btn.classList.add('active');
        const lang = btn.dataset.lang;
        renderContent(lang);
        document.documentElement.lang = lang; // Accessibility update
    });
});
